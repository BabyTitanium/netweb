package com.ps.net.model;

import java.util.Date;

public class RoomDataSource {
    private Integer zone_id;

    private Integer flow;

    private Integer in_temp;

    private Integer out_temp;

    private Integer room_temp;

    private Date update_time;

    public Integer getZone_id() {
        return zone_id;
    }

    public void setZone_id(Integer zone_id) {
        this.zone_id = zone_id;
    }

    public Integer getFlow() {
        return flow;
    }

    public void setFlow(Integer flow) {
        this.flow = flow;
    }

    public Integer getIn_temp() {
        return in_temp;
    }

    public void setIn_temp(Integer in_temp) {
        this.in_temp = in_temp;
    }

    public Integer getOut_temp() {
        return out_temp;
    }

    public void setOut_temp(Integer out_temp) {
        this.out_temp = out_temp;
    }

    public Integer getRoom_temp() {
        return room_temp;
    }

    public void setRoom_temp(Integer room_temp) {
        this.room_temp = room_temp;
    }

    public Date getUpdate_time() {
        return update_time;
    }

    public void setUpdate_time(Date update_time) {
        this.update_time = update_time;
    }
}