package com.ps.net.model;

public class Builds {
    private Integer id;

    private String build_name;

    private Integer build_num;

    private Integer zone_id;

    private Integer cal_mode;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getBuild_name() {
        return build_name;
    }

    public void setBuild_name(String build_name) {
        this.build_name = build_name == null ? null : build_name.trim();
    }

    public Integer getBuild_num() {
        return build_num;
    }

    public void setBuild_num(Integer build_num) {
        this.build_num = build_num;
    }

    public Integer getZone_id() {
        return zone_id;
    }

    public void setZone_id(Integer zone_id) {
        this.zone_id = zone_id;
    }

    public Integer getCal_mode() {
        return cal_mode;
    }

    public void setCal_mode(Integer cal_mode) {
        this.cal_mode = cal_mode;
    }
}